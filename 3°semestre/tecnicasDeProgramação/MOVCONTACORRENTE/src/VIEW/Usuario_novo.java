    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
//CADASTRAR DADOS
        String operacao = "Incluir";
        if (operacaoAtivaGlobal.equals(operacao)){
        Usuarios dados_usuarios = new Usuarios();
        dados_usuarios.setIdCli(jTextField1.getText());
        dados_usuarios.setSenha(jTextField2.getText());
        dados_usuarios.setNum_ag1(jTextField3.getText());
        dados_usuarios.setNum_cc1(jTextField4.getText());
        
        connectDAO objcon = new connectDAO();
        
        objcon.insereRegistroJFBD("USUARIOS", dados_usuarios.dadosSQLValues());
        
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

